class Color {

    static returnColor(num) {

        if (num == 1) {
            return 'red';
        } else if (num==2) {
            return 'white';
        } else if (num==3) {
            return 'blue';
        } else if (num==4) {
            return 'green';
        } else if (num==5) {
            return 'hotpink';
        } else if (num==6) {
            return 'yellow'
        }

    }
}

module.exports = Color;